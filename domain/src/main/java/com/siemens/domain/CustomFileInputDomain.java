package com.siemens.domain;

public class CustomFileInputDomain
{
    String units;
    String section;
    String asset;
    String assetsKksTag;
    String type;
    String building;
    double x;
    double y;
    double z;
    
    public String getUnits()
    {
        return units;
    }
    
    public void setUnits(String units)
    {
        this.units = units;
    }
    
    public String getSection()
    {
        return section;
    }
    
    public void setSection(String section)
    {
        this.section = section;
    }
    
    public String getAsset()
    {
        return asset;
    }
    
    public void setAsset(String asset)
    {
        this.asset = asset;
    }
    
    public String getAssetsKksTag()
    {
        return assetsKksTag;
    }
    
    public void setAssetsKksTag(String assetsKksTag)
    {
        this.assetsKksTag = assetsKksTag;
    }
    
    public String getType()
    {
        return type;
    }
    
    public void setType(String type)
    {
        this.type = type;
    }
    
    public String getBuilding()
    {
        return building;
    }
    
    public void setBuilding(String building)
    {
        this.building = building;
    }
    
    public double getX()
    {
        return x;
    }
    
    public void setX(double x)
    {
        this.x = x;
    }
    
    public double getY()
    {
        return y;
    }
    
    public void setY(double y)
    {
        this.y = y;
    }
    
    public double getZ()
    {
        return z;
    }
    
    public void setZ(double z)
    {
        this.z = z;
    }
    
}
